#include <stdio.h>

// Function to swap two elements
void swap(int *a, int *b) {
    int temp = *a;
    *a = *b;
    *b = temp;
}

// Bubble Sort Algorithm
void bubbleSort(int arr[], int n) {
    for (int i = 0; i < n - 1; i++) {
        for (int j = 0; j < n - i - 1; j++) {
            if (arr[j] > arr[j + 1]) {
                swap(&arr[j], &arr[j + 1]);
            }
        }
    }
}

// Selection Sort Algorithm
void selectionSort(int arr[], int n) {
    for (int i = 0; i < n - 1; i++) {
        int minIndex = i;
        for (int j = i + 1; j < n; j++) {
            if (arr[j] < arr[minIndex]) {
                minIndex = j;
            }
        }
        swap(&arr[minIndex], &arr[i]);
    }
}

// Insertion Sort Algorithm
void insertionSort(int arr[], int n) {
    for (int i = 1; i < n; i++) {
        int key = arr[i];
        int j = i - 1;
        while (j >= 0 && arr[j] > key) {
            arr[j + 1] = arr[j];
            j--;
        }
        arr[j + 1] = key;
    }
}

// Function to print an array
void printArray(int arr[], int n) {
    for (int i = 0; i < n; i++) {
        printf("%d ", arr[i]);
    }
    printf("\n");
}

// Main function to test sorting algorithms
int main() {
    int arr[] = {64, 25, 12, 22, 11};
    int n = sizeof(arr) / sizeof(arr[0]);

    printf("Original array: ");
    printArray(arr, n);

    // Bubble Sort
    int bubbleArr[] = {64, 25, 12, 22, 11};
    bubbleSort(bubbleArr, n);
    printf("Sorted array using Bubble Sort: ");
    printArray(bubbleArr, n);

    // Selection Sort
    int selectionArr[] = {64, 25, 12, 22, 11};
    selectionSort(selectionArr, n);
    printf("Sorted array using Selection Sort: ");
    printArray(selectionArr, n);

    // Insertion Sort
    int insertionArr[] = {64, 25, 12, 22, 11};
    insertionSort(insertionArr, n);
    printf("Sorted array using Insertion Sort: ");
    printArray(insertionArr, n);

    return 0;
}
